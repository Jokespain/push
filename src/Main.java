public class Main {
    public static void main(String[] args) {
        for (int i = 1; i <= 5; i++) {
            System.out.println("Значення і: " + i);
        }
        int x = 1;
        while (x <= 5) {
            System.out.println("Значення x: " + x);
            x++;
        }
        int y = 1;
        do {
            System.out.println("Значення y: " + y);
            y++;
        } while (y <= 5);

    }
}